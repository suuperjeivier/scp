package dto.user;

public class UserConfigDTO {
	private int idUserConfig;	
	private String nombre;
	private String userInitService;
	private int status;
	
	public int getIdUserConfig() {
		return idUserConfig;
	}
	public void setIdUserConfig(int idUserConfig) {
		this.idUserConfig = idUserConfig;
	}
	public String getUserInitService() {
		return userInitService;
	}
	public void setUserInitService(String userInitService) {
		this.userInitService = userInitService;
	}
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * @return the status
	 */
	public int getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(int status) {
		this.status = status;
	}

}
