package dto.listados;

public class GeneroDTO {
	
	private int idGenero;
	private String identificadorGenero;
	private String nombreGenero;
	
	public int getIdGenero() {
		return idGenero;
	}
	
	public void setIdGenero(int idGenero) {
		this.idGenero = idGenero;
	}

	public String getIdentificadorGenero() {
		return identificadorGenero;
	}

	public void setIdentificadorGenero(String identificadorGenero) {
		this.identificadorGenero = identificadorGenero;
	}

	public String getNombreGenero() {
		return nombreGenero;
	}

	public void setNombreGenero(String nombreGenero) {
		this.nombreGenero = nombreGenero;
	}
		
}
