package dto.listados.items;

public class LineaItemDTO {

	private int idLinea;
	private String nombreLinea;
	private MarcaItemDTO marcaDTO;
	private TipoItemDTO tipoItemDTO;
	
	public LineaItemDTO(){
		setMarcaDTO(new MarcaItemDTO());
		setTipoItemDTO(new TipoItemDTO());
	}
	
	/**
	 * @return the idLinea
	 */
	public int getIdLinea() {
		return idLinea;
	}
	/**
	 * @param idLinea the idLinea to set
	 */
	public void setIdLinea(int idLinea) {
		this.idLinea = idLinea;
	}
	/**
	 * @return the nombreLinea
	 */
	public String getNombreLinea() {
		return nombreLinea;
	}
	/**
	 * @param nombreLinea the nombreLinea to set
	 */
	public void setNombreLinea(String nombreLinea) {
		this.nombreLinea = nombreLinea;
	}
	/**
	 * @return the marcaDTO
	 */
	public MarcaItemDTO getMarcaDTO() {
		return marcaDTO;
	}
	/**
	 * @param marcaDTO the marcaDTO to set
	 */
	public void setMarcaDTO(MarcaItemDTO marcaDTO) {
		this.marcaDTO = marcaDTO;
	}

	/**
	 * @return the tipoItemDTO
	 */
	public TipoItemDTO getTipoItemDTO() {
		return tipoItemDTO;
	}

	/**
	 * @param tipoItemDTO the tipoItemDTO to set
	 */
	public void setTipoItemDTO(TipoItemDTO tipoItemDTO) {
		this.tipoItemDTO = tipoItemDTO;
	}
	
}
