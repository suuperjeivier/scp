package dto.listados;

public class DepartamentoDTO {
	
	private int claveDepartamento;
	private String nombreDepartamento;
	private String siglasDepartamento;
	
	public int getClaveDepartamento() {
		return claveDepartamento;
	}
	
	public void setClaveDepartamento(int claveDepartamento) {
		this.claveDepartamento = claveDepartamento;
	}

	public String getNombreDepartamento() {
		return nombreDepartamento;
	}

	public void setNombreDepartamento(String nombreDepartamento) {
		this.nombreDepartamento = nombreDepartamento;
	}

	/**
	 * @return the siglasDepartamento
	 */
	public String getSiglasDepartamento() {
		return siglasDepartamento;
	}

	/**
	 * @param siglasDepartamento the siglasDepartamento to set
	 */
	public void setSiglasDepartamento(String siglasDepartamento) {
		this.siglasDepartamento = siglasDepartamento;
	}

}
