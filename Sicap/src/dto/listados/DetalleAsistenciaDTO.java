package dto.listados;

import java.time.LocalDate;
import java.time.LocalTime;

public class DetalleAsistenciaDTO {
	
	private TablaPersonalDTO cveEmpleado;
	private LocalDate fechaAsistenciaEmpleado;
	private LocalTime horaAsistenciaEmpleado;
	
	public TablaPersonalDTO getCveEmpleado() {
		return cveEmpleado;
	}
	
	public void setCveEmpleado(TablaPersonalDTO cveEmpleado) {
		this.cveEmpleado = cveEmpleado;
	}

	public LocalDate getFechaAsistenciaEmpleado() {
		return fechaAsistenciaEmpleado;
	}

	public void setFechaAsistenciaEmpleado(LocalDate fechaAsistenciaEmpleado) {
		this.fechaAsistenciaEmpleado = fechaAsistenciaEmpleado;
	}

	public LocalTime getHoraAsistenciaEmpleado() {
		return horaAsistenciaEmpleado;
	}

	public void setHoraAsistenciaEmpleado(LocalTime horaAsistenciaEmpleado) {
		this.horaAsistenciaEmpleado = horaAsistenciaEmpleado;
	}

}
