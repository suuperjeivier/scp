package dto.listados;

public class ConfigCampoDTO {
	
	private String nombreMostrar;
	private boolean mostrar;
	private boolean deshabilitado;
	private boolean requerido;
	private String placeHolder;
	/**
	 * @return the nombreMostrar
	 */
	public String getNombreMostrar() {
		return nombreMostrar;
	}
	/**
	 * @param nombreMostrar the nombreMostrar to set
	 */
	public void setNombreMostrar(String nombreMostrar) {
		this.nombreMostrar = nombreMostrar;
	}
	/**
	 * @return the mostrar
	 */
	public boolean isMostrar() {
		return mostrar;
	}
	/**
	 * @param mostrar the mostrar to set
	 */
	public void setMostrar(boolean mostrar) {
		this.mostrar = mostrar;
	}
	/**
	 * @return the deshabilitado
	 */
	public boolean isDeshabilitado() {
		return deshabilitado;
	}
	/**
	 * @param deshabilitado the deshabilitado to set
	 */
	public void setDeshabilitado(boolean deshabilitado) {
		this.deshabilitado = deshabilitado;
	}
	/**
	 * @return the requerido
	 */
	public boolean isRequerido() {
		return requerido;
	}
	/**
	 * @param requerido the requerido to set
	 */
	public void setRequerido(boolean requerido) {
		this.requerido = requerido;
	}
	/**
	 * @return the placeHolder
	 */
	public String getPlaceHolder() {
		return placeHolder;
	}
	/**
	 * @param placeHolder the placeHolder to set
	 */
	public void setPlaceHolder(String placeHolder) {
		this.placeHolder = placeHolder;
	}
	
}
