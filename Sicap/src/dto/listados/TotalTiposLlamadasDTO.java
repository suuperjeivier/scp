package dto.listados;

public class TotalTiposLlamadasDTO {
	
	private int totalLlamadas;
	private TipoLlamadaDTO tipoLlamada;
	
	public TotalTiposLlamadasDTO(){
		this.setTipoLlamada(new TipoLlamadaDTO());
	}
	
	/**
	 * @return the totalLlamadas
	 */
	public int getTotalLlamadas() {
		return totalLlamadas;
	}
	/**
	 * @param totalLlamadas the totalLlamadas to set
	 */
	public void setTotalLlamadas(int totalLlamadas) {
		this.totalLlamadas = totalLlamadas;
	}
	/**
	 * @return the tipoLlamada
	 */
	public TipoLlamadaDTO getTipoLlamada() {
		return tipoLlamada;
	}
	/**
	 * @param tipoLlamada the tipoLlamada to set
	 */
	public void setTipoLlamada(TipoLlamadaDTO tipoLlamada) {
		this.tipoLlamada = tipoLlamada;
	}

}
